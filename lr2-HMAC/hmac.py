import pyautogui
from PIL import ImageGrab

def get_pixel_rgb():
    # Get the current mouse position
    x, y = pyautogui.position()
    
    # Capture the screen at the mouse position
    screen = ImageGrab.grab(bbox=(x, y, x+1, y+1))
    
    # Get the RGB value of the pixel
    pixel = screen.load()
    r, g, b = pixel[0, 0]
    
    return x, y, r, g, b

def get_salt(byte_len) -> int:
    salt = 0
    bitlen = byte_len * 8
    while bitlen > 0:
        prev_x, prev_y, prev_r, prev_g, prev_b = None, None, None, None, None
        while True:
            x, y, r, g, b = get_pixel_rgb()
            if (x, y, r, g, b) != (prev_x, prev_y, prev_r, prev_g, prev_b):
                salt = (salt << 1) | (x & 0x01)
                salt = (salt << 1) | (y & 0x01)
                salt = (salt << 1) | (r & 0x01)
                salt = (salt << 1) | (g & 0x01)
                salt = (salt << 1) | (b & 0x01)
                bitlen -= 5
                break
    # cut the excessive bits
    salt = salt >> (bitlen * -1)
    return salt


